
public class SingleDimensionalArray {
	public static void main(String[] args) {

		int [][] arr=new int[3][2];
		arr[0][0]=10;
		arr[0][1]=20;
		
		arr[1][0]=10;
		arr[1][1]=20;
		
		arr[2][0]=10;
		arr[2][1]=20;
		
		
		
		for(int i=0;i<
				arr.length;i++) {
			int[] singlerow=arr[i];
			for(int j=0;j<singlerow.length;j++) {
				System.out.print(singlerow[j]+"  ");
				
			}
			System.out.println();
			
			
			
			
			
			
			
			
			 
		}
		
			
			
		}
		
 		
		
		
	}


